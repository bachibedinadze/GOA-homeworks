# ციკლი, რომელიც 5-ჯერ დასვამს კითხვას რიცხვის შესახებ
numbers = []  # სიაში შევინახავთ ყველა მიღებულ რიცხვს
for _ in range(5):
    number = int(input("შეიყვანეთ რიცხვი: "))  # მომხმარებლისგან რიცხვის მიღება
    numbers.append(number)  # რიცხვის დამატება სიაში

# 5-ჯერ დაბეჭდვა
for num in numbers:
    print(num)