# მომხმარებელს შეატანინეთ მთავარი სტრინგი
main_str = input("შეიყვანეთ მთავარი სტრინგი: ")

# მომხმარებელს შეატანინეთ დასათვლელი სტრინგი
str_to_count = input("შეიყვანეთ დასათვლელი სტრინგი: ")

# გამოთვალეთ რამდენჯერ გვხვდება str_to_count main_str-ში
count = main_str.count(str_to_count)

# დაბეჭდეთ შედეგი
print(f"'{str_to_count}' встречается {count}ჯერ '{main_str}'-ში.")