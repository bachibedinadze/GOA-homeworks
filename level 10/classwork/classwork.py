# შედეგების დაბეჭდვა
print(f"პირველი მნიშვნელობის მონაცემთა ტიპი: {type(num1)}")
print(f"მეორე მნიშვნელობის მონაცემთა ტიპი: {type(num2)}")
print(f"მესამე მნიშვნელობის მონაცემთა ტიპი: {type(num3)}")