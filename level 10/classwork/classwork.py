num1 = input("გთხოვთ, შეიყვანოთ პირველი მნიშვნელობა (მთელი რიცხვი): ")
num2 = input("გთხოვთ, შეიყვანოთ მეორე მნიშვნელობა (ათწილიანი რიცხვი): ")
num3 = input("გთხოვთ, შეიყვანოთ მესამე მნიშვნელობა (სტრიქონი): ")

# კონვერტაცია შესაბამის მონაცემთა ტიპებში
