# მომხმარებლისგან ორი რიცხვის მიღება
num1 = int(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = int(input("შეიყვანეთ მეორე რიცხვი: "))

# ინიციალიზაცია რიცხვების ჯამი
total_sum = 0

# თუ პირველი რიცხვი მეტია მეორეზე, დაბეჭდეთ ლუწი რიცხვები
if num1 > num2:
    for num in range(num2, num1 + 1):
        if num % 2 == 0:  # თუ რიცხვი ლუწია
            print(num)
            total_sum += num

# თუ მეორე რიცხვი მეტია პირველზე, დაბეჭდეთ კენტი რიცხვები
elif num2 > num1:
    for num in range(num1, num2 + 1):
        if num % 2 != 0:  # თუ რიცხვი კენტია
            print(num)
            total_sum += num

# თუ რიცხვები თანაბარია
else:
    print("ორივე რიცხვი ერთნაირია.")

# დაბეჭდეთ ჯამი
print(f"ჯამი: {total_sum}")